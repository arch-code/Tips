# Python - Any operator

oneTrue = [True, False]
allTrue = [True, True]
allFalse = [False, False]

if all(oneTrue):
    print("At least one True") # This will be executed
else:
    print("None of them are True")

if all(allTrue):
    print("At least one True") # This will be executed
else:
    print("None of them are True")

if all(allFalse):
    print("At least one True")
else:
    print("None of them are True") # This will be executed


