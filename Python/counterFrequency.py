# Python - Count character frequency on string

from collections import Counter

# Count the frequency of each character
characterCount = Counter("aa b ccc d eeee!")

print(characterCount)

'''
    Output:
        Counter({
            ' ': 4,
            'e': 4,
            'c': 3,
            'a': 2,
            'b': 1,
            '!': 1,
        })
'''


